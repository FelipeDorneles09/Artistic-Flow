import { Order } from '@/types/order';

const today = new Date();
const addDays = (days: number) => {
  const date = new Date(today);
  date.setDate(date.getDate() + days);
  return date;
};

const subtractDays = (days: number) => {
  const date = new Date(today);
  date.setDate(date.getDate() - days);
  return date;
};

